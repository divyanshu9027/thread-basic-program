class hii{
    public void show() {
        for(int i=1;i<=5;i++) {
            System.out.println("hii divyanshu sharma");
        }

    }

}
class hello {
    public void show() {
        for(int i=1;i<=5;i++) {
            System.out.println("hello divyanshu sharma");

        }
    }
}
public class ThreadDemo {
    public static void main(String[] args) {
        hii divyanshu=new hii();
        hello divyanshu2=new hello();
        divyanshu.show();
        divyanshu2.show();

    }
}
